  MX_TIM3_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
  BOP_Init();
  /* USER CODE END 2 */

  int mainMCUCheck = 1;

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1)
	{
		if (Main_MCU_HeartBeat == 1) {
			Main_MCU_HeartBeat = 0;      // Reset heartbeat
			Main_MCU_HeartBeat_Counter = 0; // Reset the counter
			HAL_Delay(1000);
			continue;
		}

		Main_MCU_HeartBeat_Counter++;

		if (Main_MCU_HeartBeat_Counter > mainMCUCheck){
			Main_MCU_Off=1;
			emergencyOFF=1;
			purge_subMCU = 1;
		}

		if(Main_MCU_Off) {
			mainMCUoff(); // Shut down Main MCU
			EmergencySTOP();
			CAN_Tx(); // Start CAN after shutting down Main MCU
		}

		HAL_Delay(1000);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	}
